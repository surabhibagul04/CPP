/*1. Write a C++ program to accept two numbers and perform addition, then display the result. */

#include <iostream>
using namespace std;

int main()
{
    int a, b;
    cout << "Enter two numbers: ";
    cin >> a >> b;

    cout << "Sum = " << a + b;

    return 0;
}

/*output
Enter two numbers: 34 17 
Sum = 51 */